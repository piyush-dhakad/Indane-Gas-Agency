<h2>Indane Gas Agency Rule</h2>
<p>
In exercise of the powers conferred by sections 5 and 7 of the Explosives Act, 1884 (4 of 1884) and in suppression of the Gas Cylinders Rules, 1981, except in respect things done or omitted to be done before such super‐session, the Central Government hereby nukes the following rules, namely:<br /><br />

<b>SHORT TITLE AND COMMENCEMENT.</b>
These rules may be called the Gas Cylinders Rules, 2004.
They shall come into force on the date of their publication in the Official Gazette.<br /><br />

<b>DEFINITIONS.</b>
<br />
In these rules, unless the context otherwise requires, ‐
"Act" means the Explosives Act, 1884 (4 of 1884);
"Auto LPG" means liquefied petroleum gas meant for automotive fuel conforming to specification IS:14861;
"Chief Controller" means the Chief Controller of Explosives, Government of India;
"composite cylinder" means a cylinder made of resin impregnated continuous filament wound over a metallic or a non‐metallic liner. 
<br /><br />
<b>EXPLANATION</b>
<br />
."flammability range" means the difference between the minimum and maximum percentages by volume of the gas in mixture with air that forms a flammable mixture;
"Form" means a Form set forth in Schedule V;
"Gas Cylinder" or "Cylinder" means any closed metal container having a volume exceeding 500 ml but not exceeding 1000 litres intended for the storage and transport of compressed gas, including any liquefied petroleum gas (LPG) container/compressed natural gas (CNG) cylinder fitted to a motor vehicle as its fuel tank but not including any other such container fitted to a special transport or under‐carriage and includes a composite cylinder, however, the water capacity of cylinders used for storage of CNG, nitrogen, compressed air, etc.
<br /><br />
<b>EXPLANATION</b>
The values of saturated vapour pressure of different gases are specified in IS:3710;
"working pressure for permanent gas" means the internal pressure of the gas in the cylinder at a temperature of 15 C;
"yield strength" means the stress corresponding to a permanent strain of 0.2 per cent of the original gauge length in a tensile test.
</p>
