<h2>Our Mission</h2>
<p>Increase in penetration of LPG to all remote and uncovered parts of the country with a view to achieve 75% coverage of the population from the present 50%. This is envisaged through Rajiv Gandhi Gramin LPG Vitaran, a small format LPG distributorship and community kitchens.
Better affordability of LPG connection for customers by providing access subsidization towards security deposit of cylinder and regulator.
Enhancing customer service through services such as SMS/IVRS booking, Toll Free Feedback System, Preferred Time Delivery and Portability of connections and products such as multi-function Pressure Regulators, and High Fuel efficient Gas Stoves.
Control diversion with the help of Anti Pilfer Device</p>

<h2>Our Products</h2>
<p>
The main product of Indane is packed Liquefied petroleum gas (LPG) cylinders. It also provides LPG through Reticulated Piped Gas System which directly pipe Indane LPG into the kitchens of modern urban customers. Through its Indane LPG distributors, Indian Oil markets several home aids and conveniences.These include electric exhaust chimneys, non-stick kitchen cookware, pressure cookers, pressure pans, oven-proof crockery, cotton aprons, gas detection alarms, prefabricated modular kitchen cabinets, Nestle ready-to-cook items, microwave and multi-wave ovens, fire extinguishers, direct-to-home receivers for free-to-air channels, FMCG items, water purifiers, LPG industrial burners and auto LPG kits.
</p>
